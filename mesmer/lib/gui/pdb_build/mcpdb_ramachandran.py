def get_raw_rama_map( path ):
	
	handle = open(path)
	data = handle.readlines()
	handle.close()
	
	if len(data) != 73:
		raise MCPDBRamaError("Error in %s, file should have 73 rows: (360/5)+1."%(path))

	ret = []
	for i in xrange(73):
		try:
			PsiA = map(float,data[i].split("\t"))
		except ValueError:
			raise MCPDBRamaError("Error in %s, row %i format error."%(path,i+1))
			
		if len(PsiA) != 73:
			raise MCPDBRamaError("Error in %s, row %i should have 73 columns: (360/5)+1."%(path,i+1))

		ret.append( [sum(PsiA),PsiA] )
						
	return ret
	
def make_rama_CDFs( _map, Phi_rv_kwargs={}, Psi_rv_kwargs={} ):
	Psi_tot = float(sum(zip(*_map)[0]))
	Psi_CDF = stats.rv_discrete(a=-180, b=180, 
		values=(range(-180,185,5),[f / Psi_tot for f in zip(*_map)[0]]),
		**Psi_rv_kwargs)

	Phi_CDFs = [None]*73
	for i in xrange(73):		
		if _map[i][0] > 0.0:
			Phi_CDFs[i] = stats.rv_discrete(a=-180, b=180,
				values=(range(-180,185,5),[f / float(_map[i][0]) for f in _map[i][1]]),
				**Psi_rv_kwargs)

	return Psi_CDF,Phi_CDFs

